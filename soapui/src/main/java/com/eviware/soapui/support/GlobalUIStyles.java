package com.eviware.soapui.support;

/**
 * This class is a holder of global UI styles
 */
public interface GlobalUIStyles {
    /**
     * Main toolbar height (on the main screen)
     */
    int MAIN_TOOLBAR_HEIGHT = 50;

    /**
     * Standard height for big toolbars
     */
    int BIG_TOOLBAR_HEIGHT = 50;

    /**
     * DPI unaware standard text box height
     */
    int TEXT_BOX_STANDARD_HEIGHT = 23;
}
